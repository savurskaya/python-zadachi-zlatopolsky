d = int(input())
if d == 1:
    print('понедельник')
elif d == 2:
    print('вторник')
elif d == 3:
    print('среда')
elif d == 4:
    print('четверг')
elif d == 5:
    print('пятница')
elif d == 6:
    print('суббота')
elif d == 7:
    print('воскресенье')
else:
    print('неправильно введен день')